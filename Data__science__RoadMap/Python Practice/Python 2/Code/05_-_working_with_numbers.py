# "math" library is imported to enable work with more complex mathematical functions.
from math import *

# Print a simple integer.
print(2)

# Print a simple float.
print(-2.0987)

# Print a simple mathematical equation.
print(3 * (4 + 5))

# Modulus operator ("%") returns the remainder after division.
print(10 % 3)

# Numbers can be stored in variables, too.
my_num = 5
print(my_num)

# Numbers have to converted to strings in order to be concatenated.
print(str(my_num) + " is my favorite number.")

# "pow" function returns the first argument "powered" to the second argument.
print(pow(my_num, 2))

# "max" function compares given numbers and return the maximal one.
print(max(my_num, 4))

# "round" function rounds a given number.
print(round(3.7))

# "floor" function rounds a given number to the previous whole number.
print(floor(3.7))

# "ceil" function rounds a given number to the next whole number.
print(ceil(3.7))

# "sqrt" function returns a square root of a given number.
print(sqrt(36))
